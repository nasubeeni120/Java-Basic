package variable;

/**
 * 변수의 타입 - 다양한 타입이 존재함.
 *
 */
public class Var1 {
    public static void main(String[] args) {

        int a = 100; //정수
        double b = 10.5; //실수
        boolean c = true; //true or false -> 참 거짓 판단
        char d = 'A'; //문자 하나
        String e = "Hello Java"; //문자열 -> 큰 따옴표 사용

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
